function convert_feet_to_meter() {
    var feet = Number(prompt("Please input feet data: "));
    alert(feet + " feet equal to " + 0.305 * feet + " meter");
}

function convert_meter_to_feet() {
    var meter = Number(prompt("Please input meter data: "));
    alert(meter + " meter equal to " + 3.279 * meter + " feet");
}
